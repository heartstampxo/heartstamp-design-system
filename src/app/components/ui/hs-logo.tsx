/* ── Emblem ────────────────────────────────────────────────── */
function Emblem({ color, height }: { color: LogoColor; height: number }) {
  if (color === "dark") {
    const w = height * (239 / 241);
    return (
      <svg width={w} height={height} viewBox="0 0 239 241" fill="none" aria-label="HeartStamp emblem">
        <path d={HEART_239} fill="#242423" />
        <path d={H_DARK}    fill="#FFFFFF" />
      </svg>
    );
  }

  if (color === "light") {
    return (
      <svg width={height} height={height} viewBox="0 0 240 241" fill="none" aria-label="HeartStamp emblem">
        <path d={LIGHT_MARK}     fill="#FFFFFF" />
        <path d={LIGHT_COMBINED} fill="#FFFFFF" />
      </svg>
    );
  }

  // brand
  return (
    <svg width={height} height={height} viewBox="0 0 240 241" fill="none" aria-label="HeartStamp emblem">
      <path d={HEART_240} fill="#BE1D2C" />
      <path d={H_BRAND}   fill="#FFFFFF" />
    </svg>
  );
}

/* ── Dark-mode detection (watches CSS vars on #root) ─────── */
export function useIsDark(): boolean {
  const read = (): boolean => {
    const root = document.querySelector("#root > div") as HTMLElement | null
              ?? document.getElementById("root");
    if (!root) return false;
    const bg = root.style.getPropertyValue("--bg").trim();
    const hex = bg.replace("#", "");
    if (hex.length < 6) return false;
    const r = parseInt(hex.slice(0, 2), 16);
    const g = parseInt(hex.slice(2, 4), 16);
    const b = parseInt(hex.slice(4, 6), 16);
    return (0.299 * r + 0.587 * g + 0.114 * b) / 255 < 0.5;
  };
  const [isDark, setIsDark] = React.useState(read);
  React.useEffect(() => {
    const root = (document.querySelector("#root > div") as HTMLElement | null)
              ?? document.getElementById("root");
    if (!root) return;
    const obs = new MutationObserver(() => setIsDark(read()));
    obs.observe(root, { attributes: true, attributeFilter: ["style"] });
    return () => obs.disconnect();
  }, []);
  return isDark;
}

/* ── Lockup ────────────────────────────────────────────────── */
function Lockup({ color, height }: { color: LogoColor; height: number }) {
  const ref = React.useRef<HTMLSpanElement>(null);
  const appIsDark = useIsDark();
  const [localDark, setLocalDark] = React.useState(false);

  React.useEffect(() => {
    const check = () => {
      if (!ref.current) return;
      const bg = getComputedStyle(ref.current).getPropertyValue("--bg").trim();
      if (bg.startsWith("#") && bg.length >= 7) {
        const r = parseInt(bg.slice(1, 3), 16);
        const g = parseInt(bg.slice(3, 5), 16);
        const b = parseInt(bg.slice(5, 7), 16);
        setLocalDark((0.299 * r + 0.587 * g + 0.114 * b) / 255 < 0.5);
      }
    };
    check();
    const mo = new MutationObserver(check);
    mo.observe(document.body, { subtree: true, attributes: true, attributeFilter: ["style", "class"] });
    return () => mo.disconnect();
  }, []);

  const useDark = color === "brand" && (appIsDark || localDark);
  const w = height * (456 / 114);

  if (useDark) {
    return (
      <span ref={ref} style={{ display: "inline-flex" }}>
        <img src={lockupDark} width={w} height={height} alt="HeartStamp" style={{ display: "block" }} />
      </span>
    );
  }

  const { heart, fg } = LOCKUP_FILLS[color] ?? LOCKUP_FILLS["brand"];
  return (
    <span ref={ref} style={{ display: "inline-flex" }}>
      <svg
        width={w}
        height={height}
        viewBox="0 0 456 114"
        fill="none"
        aria-label="HeartStamp"
      >
        <path d={LOCKUP_HEART} fill={heart} />
        {LOCKUP_LETTERS.map((d, i) => <path key={i} d={d} fill={fg} />)}
        <path d={LOCKUP_H} fill={fg} />
      </svg>
    </span>
  );
}

/* ── HSLogo — unified component ────────────────────────────── */
export function HSLogo({
  type    = "lockup",
  color   = "brand",
  height  = 40,
  style,
  className,
}: HSLogoProps) {
  const el =
    type === "emblem"
      ? <Emblem color={color} height={height} />
      : <Lockup color={color} height={height} />;

  return (style || className)
    ? <div style={style} className={className}>{el}</div>
    : el;
}

