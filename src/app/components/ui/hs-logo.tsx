const EMBLEM_BRAND_FILLS  = { heart: "#BE1D2C", h: "#FFFFFF" };
const EMBLEM_DARK_FILLS   = { heart: "#242423", h: "#FFFFFF" };
const EMBLEM_LIGHT_FILLS  = { mark: "#FFFFFF",  combined: "#FFFFFF" };

export function getSvgString(type: LogoType, color: LogoColor): string {
  if (type === "emblem") {
    if (color === "dark") {
      return `<svg width="239" height="241" viewBox="0 0 239 241" fill="none" xmlns="http://www.w3.org/2000/svg"><path d="${HEART_239}" fill="${EMBLEM_DARK_FILLS.heart}"/><path d="${H_DARK}" fill="${EMBLEM_DARK_FILLS.h}"/></svg>`;
    }
    if (color === "light") {
      return `<svg width="240" height="241" viewBox="0 0 240 241" fill="none" xmlns="http://www.w3.org/2000/svg"><path d="${LIGHT_MARK}" fill="${EMBLEM_LIGHT_FILLS.mark}"/><path d="${LIGHT_COMBINED}" fill="${EMBLEM_LIGHT_FILLS.combined}"/></svg>`;
    }
    // brand
    return `<svg width="240" height="241" viewBox="0 0 240 241" fill="none" xmlns="http://www.w3.org/2000/svg"><path d="${HEART_240}" fill="${EMBLEM_BRAND_FILLS.heart}"/><path d="${H_BRAND}" fill="${EMBLEM_BRAND_FILLS.h}"/></svg>`;
  }

  // lockup (brand-dark uses brand fills but with white text)
  const fills = LOCKUP_FILLS[color] ?? LOCKUP_FILLS["brand-dark"];
  const letterPaths = LOCKUP_LETTERS.map(d => `<path d="${d}" fill="${fills.fg}"/>`).join("");
  return `<svg width="456" height="114" viewBox="0 0 456 114" fill="none" xmlns="http://www.w3.org/2000/svg"><path d="${LOCKUP_HEART}" fill="${fills.heart}"/>${letterPaths}<path d="${LOCKUP_H}" fill="${fills.fg}"/></svg>`;
}
